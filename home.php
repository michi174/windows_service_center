In Kürze wird hier ein Portal entstehen, dass sich mit der Thematik &quot;Windows&quot; und weiteren Problematiken rund um den PC befassen wird.<br />
<br />
Grundsätzlich ist wird dieses Portal einen großen Eigennutzen für mich persönlich haben, da ich viele PC's, Notebooks udgl. zu reparieren habe.<br />
Um mir diese Arbeit so einfach wie möglich zu gestalten, werde ich viele nützliche Dinge auf dieser Website veröffentliche, um diese auch unterwegs abrufen zu können. <br />
<br />
Jedoch werde ich auch einige kompliziertere Themen behandeln, über die ich eigene Artikel schreiben möchte, da ich dazu neige, für ein schwieriges Problem, immer wieder einen neuen Lösungsweg zu finden. Diese Artikel sollen helfen, bereits dagewesene Probleme einfacher lösen zu können. Diese &quot;Problemdatenbank&quot; wird dauerhaft wachsen um möglichst viele typische PC Probleme relativ einfach und konfortabel anzugehen. <br />
<br />
Um jedoch auch anderen Benutzern die Möglichkeit zu geben, an meinem Wissen teilhaben zu lassen, habe ich mich entschlossen, diese Website zu konzipieren um diese Problemlösungen auch der breiten Masse zugänglich zu machen. Mit dem Ziel eine kleine Community zu erschaffen, habe ich mich auch dazu entschlossen, ein Forum einzubauen, um speziefische Fragen einzelner User beantworten zu können. <br />
<br />
Folgende Features werde ich nach und nach in meine Website integrieren:<br />
<ul>
	<li>Artikel und Newssystem</li>
	<li>Downloads mit Bewertungen (5-Star Rating)</li>
	<li>Bildergallerie (katigorisiert)</li>
	<li>Umfragesystem</li>
	<li>Forum</li>
	<li>Benutzerverwaltung (inkl. Rechteverwaltung)</li>
	<li>Kommentare für sämtl. Bereiche</li>
	<li>Speichersystem für sensible Daten (Wichtige Passwörter, Mail-Zugangsdaten, usw.)</li>
	<li>Eventuell persönliche Microsoft&trade; Skydrive Einbundung</li>
	<li>Windows-Live Kalenderunterstützung (Cloud!)</li>
	<li>Version für Mobile Endgeräte (Windows Phone 8)</li>
</ul>
<br />
Als Design für diese Website orientiere ich mich an der Oberfläche von Windows 8, dem sogenannten &quot;Modern UI&quot;, da dieses schlichte und zugleich moderne, zeitlose Design perfekt zur Thematik dieser Website passt.
Des Weiteren, werde ich diese website erstmals nicht im klassischem, veraltetem Tabellenformat gestalten, sondern versuche mich an moderneren DIV-Containern in Verbindung mit CSS, welche weitaus mehr gestalterische Freiheit lassen.<br />
<br />
Ich hoffen, mit desem Beitrag, konnte ich Ihnen einen kleinen Überblick über mein Vorhaben verschaffen und freue mich, Ihre zukünftige Anlaufstelle bei PC-Problemen zu sein.<br />
